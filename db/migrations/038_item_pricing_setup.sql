INSERT INTO public.item_pricing_rule_set_breaks (
  rule_set_id,
  decoration_method_id,
  quantity_break_id,
  base_reference,
  prior_quantity_break_id,
  adder_amount,
  calculation_order,
  active,
  notes,
  created_by,
  updated_by
)
SELECT
  rs.id,
  dm.id,
  qb.id,
  seed.base_reference,
  prior_qb.id,
  seed.adder_amount,
  seed.calculation_order,
  true,
  seed.notes,
  'system',
  'system'
FROM seed
JOIN public.item_pricing_rule_sets rs
  ON rs.code = seed.rule_set_code
JOIN public.item_pricing_decoration_methods dm
  ON dm.code = seed.method_code
JOIN public.item_pricing_quantity_breaks qb
  ON qb.code = seed.qty_code
LEFT JOIN public.item_pricing_quantity_breaks prior_qb
  ON prior_qb.code = seed.prior_qty_code
WHERE NOT EXISTS (
  SELECT 1
  FROM public.item_pricing_rule_set_breaks existing
  WHERE existing.rule_set_id = rs.id
    AND existing.decoration_method_id = dm.id
    AND existing.quantity_break_id = qb.id
    AND existing.active = true
);
