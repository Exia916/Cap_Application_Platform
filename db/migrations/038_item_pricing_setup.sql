-- 038_item_pricing_setup.sql
-- Phase 1 foundation for CAP Item Pricing Setup.
-- Source pricing model: Blank EQP 2500+ is stored as the item base price.
-- Flat EQP = Blank EQP + 3.00.
-- 3D EQP = Blank EQP + 5.75, calculated directly from Blank EQP.

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS public.item_pricing_price_books (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code text NOT NULL,
  name text NOT NULL,
  description text NULL,
  status text NOT NULL DEFAULT 'DRAFT',
  effective_date date NULL,
  expiration_date date NULL,
  is_default boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  is_voided boolean NOT NULL DEFAULT false,
  voided_at timestamptz NULL,
  voided_by text NULL,
  void_reason text NULL,
  CONSTRAINT item_pricing_price_books_status_chk
    CHECK (status IN ('DRAFT', 'REVIEW', 'PUBLISHED', 'ARCHIVED')),
  CONSTRAINT item_pricing_price_books_dates_chk
    CHECK (expiration_date IS NULL OR effective_date IS NULL OR expiration_date >= effective_date)
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_item_pricing_price_books_code_active
  ON public.item_pricing_price_books (upper(code))
  WHERE is_voided = false;

CREATE UNIQUE INDEX IF NOT EXISTS ux_item_pricing_price_books_default_active
  ON public.item_pricing_price_books (is_default)
  WHERE is_default = true AND is_voided = false;

CREATE TABLE IF NOT EXISTS public.item_pricing_rule_sets (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL UNIQUE,
  name text NOT NULL,
  description text NULL,
  allows_blank boolean NOT NULL DEFAULT true,
  allows_flat_emb boolean NOT NULL DEFAULT true,
  allows_3d_emb boolean NOT NULL DEFAULT false,
  allows_knit_in boolean NOT NULL DEFAULT false,
  sort_order integer NOT NULL DEFAULT 0,
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL
);

CREATE TABLE IF NOT EXISTS public.item_pricing_decoration_methods (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL UNIQUE,
  name text NOT NULL,
  description text NULL,
  sort_order integer NOT NULL DEFAULT 0,
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  CONSTRAINT item_pricing_decoration_methods_code_chk
    CHECK (code IN ('BLANK', 'FLAT_EMB', 'THREE_D_EMB', 'KNIT_IN'))
);

CREATE TABLE IF NOT EXISTS public.item_pricing_quantity_breaks (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL UNIQUE,
  label text NOT NULL,
  min_qty integer NOT NULL,
  max_qty integer NULL,
  sort_order integer NOT NULL DEFAULT 0,
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  CONSTRAINT item_pricing_quantity_breaks_qty_chk
    CHECK (min_qty > 0 AND (max_qty IS NULL OR max_qty >= min_qty))
);

CREATE TABLE IF NOT EXISTS public.item_pricing_rule_set_breaks (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  rule_set_id integer NOT NULL REFERENCES public.item_pricing_rule_sets(id) ON DELETE CASCADE,
  decoration_method_id integer NOT NULL REFERENCES public.item_pricing_decoration_methods(id),
  quantity_break_id integer NOT NULL REFERENCES public.item_pricing_quantity_breaks(id),
  base_reference text NOT NULL,
  prior_quantity_break_id integer NULL REFERENCES public.item_pricing_quantity_breaks(id),
  adder_amount numeric(12, 4) NOT NULL DEFAULT 0,
  calculation_order integer NOT NULL DEFAULT 0,
  active boolean NOT NULL DEFAULT true,
  notes text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  CONSTRAINT item_pricing_rule_set_breaks_base_ref_chk
    CHECK (base_reference IN ('BLANK_EQP', 'FLAT_EQP', 'THREE_D_EQP', 'PRIOR_BREAK', 'KNIT_IN_EQP')),
  CONSTRAINT item_pricing_rule_set_breaks_prior_chk
    CHECK (
      (base_reference = 'PRIOR_BREAK' AND prior_quantity_break_id IS NOT NULL)
      OR (base_reference <> 'PRIOR_BREAK')
    )
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_item_pricing_rule_set_breaks_active
  ON public.item_pricing_rule_set_breaks (rule_set_id, decoration_method_id, quantity_break_id)
  WHERE active = true;

CREATE INDEX IF NOT EXISTS ix_item_pricing_rule_set_breaks_rule_set
  ON public.item_pricing_rule_set_breaks (rule_set_id, decoration_method_id, calculation_order);

CREATE TABLE IF NOT EXISTS public.item_pricing_items (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  item_code text NOT NULL,
  item_description text NULL,
  product_family text NULL,
  rule_set_id integer NOT NULL REFERENCES public.item_pricing_rule_sets(id),
  active boolean NOT NULL DEFAULT true,
  allows_blank_override boolean NULL,
  allows_flat_emb_override boolean NULL,
  allows_3d_emb_override boolean NULL,
  allows_knit_in_override boolean NULL,
  notes text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  is_voided boolean NOT NULL DEFAULT false,
  voided_at timestamptz NULL,
  voided_by text NULL,
  void_reason text NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_item_pricing_items_code_active
  ON public.item_pricing_items (upper(item_code))
  WHERE is_voided = false;

CREATE INDEX IF NOT EXISTS ix_item_pricing_items_rule_set
  ON public.item_pricing_items (rule_set_id, active, is_voided);

CREATE TABLE IF NOT EXISTS public.item_pricing_item_base_prices (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  price_book_id uuid NOT NULL REFERENCES public.item_pricing_price_books(id) ON DELETE CASCADE,
  item_id uuid NOT NULL REFERENCES public.item_pricing_items(id) ON DELETE CASCADE,
  blank_eqp_price numeric(12, 4) NOT NULL,
  source_file_name text NULL,
  source_sheet_name text NULL,
  source_row_number integer NULL,
  notes text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by text NULL,
  CONSTRAINT item_pricing_item_base_prices_blank_chk
    CHECK (blank_eqp_price >= 0),
  CONSTRAINT item_pricing_item_base_prices_source_row_chk
    CHECK (source_row_number IS NULL OR source_row_number > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_item_pricing_item_base_prices_book_item
  ON public.item_pricing_item_base_prices (price_book_id, item_id);

CREATE INDEX IF NOT EXISTS ix_item_pricing_item_base_prices_item
  ON public.item_pricing_item_base_prices (item_id);

CREATE TABLE IF NOT EXISTS public.item_pricing_calculated_prices (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  price_book_id uuid NOT NULL REFERENCES public.item_pricing_price_books(id) ON DELETE CASCADE,
  item_id uuid NOT NULL REFERENCES public.item_pricing_items(id) ON DELETE CASCADE,
  rule_set_id integer NOT NULL REFERENCES public.item_pricing_rule_sets(id),
  decoration_method_id integer NOT NULL REFERENCES public.item_pricing_decoration_methods(id),
  quantity_break_id integer NOT NULL REFERENCES public.item_pricing_quantity_breaks(id),
  blank_eqp_price numeric(12, 4) NOT NULL,
  calculated_price numeric(12, 4) NOT NULL,
  calculation_trace jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by text NULL,
  CONSTRAINT item_pricing_calculated_prices_amounts_chk
    CHECK (blank_eqp_price >= 0 AND calculated_price >= 0)
);

CREATE INDEX IF NOT EXISTS ix_item_pricing_calculated_prices_book_item
  ON public.item_pricing_calculated_prices (price_book_id, item_id, decoration_method_id, quantity_break_id);

